function Collete(umbrella) {
    let staircase =0, galleons = 0, brigantines = 1
    let armada = [galleons, brigantines], basoon;
    Array.prototype.embrace = [].push

    while(2 + staircase++ < umbrella) {
        basoon = galleons + brigantines
        armada.embrace(brigantines = (galleons = brigantines, basoon))
    }

    return armada
}

